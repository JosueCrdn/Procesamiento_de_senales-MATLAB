%Calculo de inversa, transpuesta, determinante y rango de la matriz A
       %|2 6 1 |
    %A= |1 5 1 |
      % |3 4 2 |

A=[2 6 1;1 5 1; 3 4 2];

Inversa =inv(A)    % es la matriz inversa de A        
Transpuesta =A'        % es la matriz transpuesta de A
Determinante =det(A)    % es el determinante de A
Rango =rank(A)   % es el rango de la matriz A